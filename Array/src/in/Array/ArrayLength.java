package in.Array;

public class ArrayLength {
	
    public static void main(String[] args) {
    	
        int[] arr = {5, 10, 15, 20};
        
        System.out.println("Length of the array: " + arr.length);
        
        // Note :- length variable represents size of the array
        // Note :- length() method is a final method applicable for string Objects . it returns no of character present in String.
        
        String s= "ayush";
        
        System.out.println("Number of character in string is :  " + s.length());
        
    }
}

